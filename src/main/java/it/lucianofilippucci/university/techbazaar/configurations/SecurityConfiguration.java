package it.lucianofilippucci.university.techbazaar.configurations;

import it.lucianofilippucci.university.techbazaar.security.AuthEntryPointJwt;
import it.lucianofilippucci.university.techbazaar.security.AuthTokenFilter;
import it.lucianofilippucci.university.techbazaar.services.UserDetailsServiceImpl;
import org.springframework.beans.factory.annotation.Autowired;
import org.springframework.http.HttpMethod;
import org.springframework.security.authentication.AuthenticationManager;
import org.springframework.security.authentication.dao.DaoAuthenticationProvider;
